/*---------------------------------------头文件---------------------------------------*/
#include <stdio.h>  // “标准输入输出库” ---> 提供printf、perror等输入输出函数声明
#include <stdlib.h> // “标准库” ----------> 提供exit、EXIT_FAILURE等进程控制和通用函数声明
//#include <errno.h>  // “错误处理库” ------> 定义了全局错误码变量errno，用于标识系统调用的错误类型
#include <unistd.h> // “POSIX系统调用库” -> 提供read、write、close、unlink等系统调用函数声明


/* 说明使用下面的函数的时候需要包含下面的头文件：
*       1. open
*/
#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>
 

/*---------------------------------------宏定义---------------------------------------*/
// 错误处理宏：打印错误信息并退出程序
#define ERR_EXIT(m)         \
    do                      \
    {                       \
        perror(m);          \
        exit(EXIT_FAILURE); \
    } while (0)


/*---------------------------------------主程序---------------------------------------*/
int main(int argc, char *argv[])
{
    /*----------------------------第一步：打开目标文件和管道文件----------------------------*/
    //1.打开目标文件"abc.bak"（只写模式，若不存在则创建，若存在则清空）
    int outfd;
    outfd = open("abc.bak", O_WRONLY | O_CREAT | O_TRUNC, 0644);
    if (outfd == -1)
        ERR_EXIT("open"); // 打开失败则报错退出

    //2.打开命名管道"tp"（只读模式）
    int infd;
    infd = open("tp", O_RDONLY);
    if (infd == -1)
        ERR_EXIT("open"); // 打开失败则报错退出

    /*----------------------------第二步：循环读取源文件内容并将其写到命名管道中----------------------------*/
    //3.定义缓冲区用于存储读取的文件内容
    char buf[1024]; 
    //4.记录每次读取的字节数
    int n;          

    //5.循环从命名管道读取内容，直到读取到0字节 <--- 管道写端关闭
    while ((n = read(infd, buf, 1024)) > 0)
    {
        //5.1：将读取的内容写入目标文件
        write(outfd, buf, n);
    }

    //6.关闭命名管道和目标文件的文件描述符
    close(infd);
    close(outfd);

    /*----------------------------第三步：删除命名管道文件----------------------------*/
    //7.删除命名管道文件"tp"避免文件系统残留
    unlink("tp");

    return 0;
}